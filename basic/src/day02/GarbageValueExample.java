package day02;

public class GarbageValueExample {
	public static void main(String[] args) {
		byte var1 = 125;
		int var2 = 125;
		
		System.out.println(var1);
		System.out.println(var2);
		
		var1++;
		var2++;
		System.out.println(var1);
		System.out.println(var2);
		
		var1++;
		var2++;
		System.out.println(var1);
		System.out.println(var2);
		
		var1++;
		var2++;
		System.out.println(var1);
		System.out.println(var2);

	}

}
