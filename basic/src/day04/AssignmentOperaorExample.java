package day04;

public class AssignmentOperaorExample {
	public static void main(String[] args) {
		int result = 0;
		result += 10;
		System.out.println("result=" + result);// +를 자기자신과 더한값을 넣는다.
		result -= 5;
		System.out.println("result=" + result);// 5= 
		result *= 3;
		System.out.println("result=" + result);
		result /= 5;
		System.out.println("result=" + result);
		result %= 3;
		System.out.println("result=" + result);
		
		
	}
}
