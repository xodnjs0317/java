package day04;

public class BitLogicExample {
	public static void main(String []args) {
		System.out.println("45 & 25 = " + (45 & 25));// 둘다1이면 1 아니면 0으로표기
		System.out.println("45 | 25 =" +(45 | 25));// 둘다 0이면0 아니면 1로표기
		System.out.println("45 ^ 25 =" +(45 ^ 25)); //두값이 같으면 1 다르면 0
		System.out.println("45 = + ~45"); //~0이면 1 ~1이면 0
		
		
		
		
	}
}
